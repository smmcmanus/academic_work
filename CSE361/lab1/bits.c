int negate(int x){
  //basic 2s complement negation
  return (~x)+1;
}
/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int divpwr2(int x, int n) {
  //if x is negative, you must add 1 to ensure value is rounded towards 0
  int isNeg = ((1 << n) + ~0) & (x >> 31);
  //add 1 if negative, 0 otherwise  
  int ans = x + isNeg;
  //left arithmatic shift completes division
  return ans >> n;
}
/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int addOK(int x, int y) {
  //check x and y sign
  int x_neg = x >> 31;
  int y_neg = y >> 31;

  //check sum sign
  int sum = x + y;
  int sum_neg = sum >> 31;
  //sum has different sign than x and y, overflow occurred
  return !(~(x_neg ^ y_neg) & (x_neg ^ sum_neg));
}
