/* 
 * isEqual - return 1 if x == y, and 0 otherwise 
 *   Examples: isEqual(5,5) = 1, isEqual(4,5) = 0
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int isEqual(int x, int y) {
  //if x and y are identical, x ^ y cancels out and returns 0
  return !(x ^ y);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  //if x then y, else z 
  //if x is non zero, j is a mask of 1s, k is a mask of zeros
  //x is zero, the reverse is true
  int i = !x;
  int j = ~i + 1;
  int k = ~j;
  //returns the value of y or z based on which of the above remained true
  //only one of the below terms will be nonzero
  return (z & j) + (y & k);
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
  //byte of all 1s
  int a = 0xff;
  int new_shift = n << 3;
  a = ~(a << new_shift);
  c = c << new_shift;
  //zero out specified byte of x and add in the new byte
  return (x & a) | c;
}
